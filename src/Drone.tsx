  const { body, nose, caps } = useMemo(() => getStaticGeometries(nodes), [nodes])

  const flightGroup = useRef<THREE.Group>(null!)
  const bladeRefs = useRef<THREE.Mesh[]>([])
  const lightRefs = useRef<THREE.PointLight[]>([])
