    bladeMaterials.forEach((mat, i) => {
      const pulse = (1.8 + Math.sin(state.clock.elapsedTime * 6 + i + phase) * 0.8 + boost * 4) * glow
      if (showColor) {
        mat.color.copy(showColor)
        mat.emissive.copy(showColor)
      } else {
        const h = (hue + t * (0.25 + boost * 0.6) + i * 0.25) % 1
        mat.color.setHSL(h, 1, 0.5 + boost * 0.15)
        mat.emissive.setHSL(h, 1, 0.5 + boost * 0.15)
      }
      mat.emissiveIntensity = pulse

      const light = lightRefs.current[i]
      if (light) {
        light.color.copy(mat.emissive)
        light.intensity = pulse
      }
    })

    const x = formationPosition.x + Math.sin(t) * wander[0]
    const z = formationPosition.z + Math.sin(t * 2) * wander[1]
    const y = formationPosition.y + Math.sin(t * 1.5) * wander[1] * 0.6
